package by.epamtc.exception;

public class TextHandlerIndexOutOfBoundsException extends Exception {

    public TextHandlerIndexOutOfBoundsException() {
    }

    public TextHandlerIndexOutOfBoundsException(String message) {
        super(message);
    }

    public TextHandlerIndexOutOfBoundsException(String message, Throwable cause) {
        super(message, cause);
    }

    public TextHandlerIndexOutOfBoundsException(Throwable cause) {
        super(cause);
    }

}
